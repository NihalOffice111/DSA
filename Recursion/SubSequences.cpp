#include<bits/stdc++.h>
using namespace std;

void generateSubSequences(string current , string str , int i ){
    if(i == str.length()){
        cout<<current<<" "<<endl;
        return;
    }

    generateSubSequences(current+str[i] ,str , i+1);

    generateSubSequences(current,str,i+1);

}

int main(){ 
    string str = "abc";

    generateSubSequences("",str,0);
    return 0;
}








































































































































































































































    
